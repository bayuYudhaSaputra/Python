# Tuliskan program yang meminta input berat dalam pound
# kemudian dikonversi ke satuan kilogram jika 
# 1 pound = 0.454 Kg
# ====================================================

# Input berat dalam pound
pound = eval(input("Input berat dalam satuan pound (Misal 55.5): "))

# Konversi berat dari pound ke kilogram
kilogram = 0.454 * pound

# Tampilkan hasil konversi
print("Massa", pound, "pound sama dengan", kilogram, "Kg.")