import turtle # import modul turtle

turtle.showturtle() 
turtle.write("Welcome to Python") # tampilkan teks
turtle.forward(100) # garis maju sesuai arah panah sepanjang 100px
turtle.right(90) # memutar arah panah 90 derajat searah jarum jam
turtle.color("red") # mengubah warna garis menjadi merah
turtle.forward(50) # garis maju sesuai arah panah sejauh 50px
turtle.right(90) # memutar arah panah 90 derajat searah jarum jam
turtle.color("green") # ubah warna hijau
turtle.forward(100) # buat garis sesuai arah panah sepanjang 100px
turtle.right(45) # memutar arah panah 45 derajat searah jarum jam
turtle.forward(80) # buat garis sesuai arah panah sepanjang 80px



